'use client';

import { useEffect, useRef, ReactNode } from 'react';
import { gsap } from 'gsap';

interface GridMotionBackgroundProps {
  items?: (string | ReactNode)[];
  gradientColor?: string;
}

export function GridMotionBackground({ 
  items = [], 
  gradientColor = 'black' 
}: GridMotionBackgroundProps) {
  const gridRef = useRef<HTMLDivElement>(null);
  const rowRefs = useRef<(HTMLDivElement | null)[]>([]);
  const mouseXRef = useRef<number>(0);
  const isMountedRef = useRef<boolean>(false);

  const totalItems = 28;
  const defaultItems: string[] = Array.from(
    { length: totalItems }, 
    (_, index) => `Item ${index + 1}`
  );
  const combinedItems = items.length > 0 ? items.slice(0, totalItems) : defaultItems;

  useEffect(() => {
    mouseXRef.current = window.innerWidth / 2;
    isMountedRef.current = true;

    gsap.ticker.lagSmoothing(0);

    const handleMouseMove = (e: MouseEvent): void => {
      mouseXRef.current = e.clientX;
    };

    const updateMotion = (): void => {
      if (!isMountedRef.current) return;

      const maxMoveAmount = 300;
      const baseDuration = 0.8;
      const inertiaFactors = [0.6, 0.4, 0.3, 0.2];

      rowRefs.current.forEach((row, index) => {
        if (row) {
          const direction = index % 2 === 0 ? 1 : -1;
          const moveAmount = 
            ((mouseXRef.current / window.innerWidth) * maxMoveAmount - maxMoveAmount / 2) * 
            direction;

          gsap.to(row, {
            x: moveAmount,
            duration: baseDuration + inertiaFactors[index % inertiaFactors.length],
            ease: 'power3.out',
            overwrite: 'auto',
          });
        }
      });
    };

    const removeAnimationLoop  = gsap.ticker.add(updateMotion);
    window.addEventListener('mousemove', handleMouseMove);

    return () => {
      isMountedRef.current = false;
      window.removeEventListener('mousemove', handleMouseMove);
      gsap.ticker.remove(removeAnimationLoop);
    };
  }, []);

  const renderContent = (content: string | ReactNode, index: number): ReactNode => {
    if (typeof content === 'string' && content.startsWith('http')) {
      return (
        <div
          key={`img-${index}`}
          className="w-full h-full bg-cover bg-center absolute top-0 left-0"
          style={{ backgroundImage: `url(${content})` }}
          aria-hidden="true"
        />
      );
    }
    return (
      <div key={`content-${index}`} className="p-4 text-center z-[1]">
        {content}
      </div>
    );
  };

  return (
    <div ref={gridRef} className="h-full w-full overflow-hidden">
      <section
        className="w-full h-screen overflow-hidden relative flex items-center justify-center"
        style={{
          background: `radial-gradient(circle, ${gradientColor} 0%, transparent 100%)`,
        }}
        aria-hidden="true"
      >
        <div className="absolute inset-0 pointer-events-none z-[4] bg-[length:250px]" />
        
        <div className="gap-4 flex-none relative w-[150vw] h-[150vh] grid grid-rows-4 grid-cols-1 rotate-[-15deg] origin-center z-[2]">
          {Array.from({ length: 4 }).map((_, rowIndex) => (
            <div
              key={`row-${rowIndex}`}
              className="grid gap-4 grid-cols-7"
              style={{ willChange: 'transform, filter' }}
              ref={(el) => {
                rowRefs.current[rowIndex] = el;
              }}
            >
              {Array.from({ length: 7 }).map((_, itemIndex) => {
                const contentIndex = rowIndex * 7 + itemIndex;
                const content = combinedItems[contentIndex];
                
                return (
                  <div key={`item-${rowIndex}-${itemIndex}`} className="relative">
                    <div className="relative w-full h-full overflow-hidden rounded-[10px] bg-[#111] flex items-center justify-center text-white text-[1.5rem]">
                      {renderContent(content, contentIndex)}
                    </div>
                  </div>
                );
              })}
            </div>
          ))}
        </div>
        
        <div className="relative w-full h-full top-0 left-0 pointer-events-none" />
      </section>
    </div>
  );
}